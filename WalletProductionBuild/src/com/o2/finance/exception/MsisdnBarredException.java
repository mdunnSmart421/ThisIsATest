package com.o2.finance.exception;

/**
 * Purpose: Thrown when the createUser call fails due to the MSISDN being barred. com.o2.finance.exception User: Vijay Kancherla
 * Date: 21/06/2011
 */
public class MsisdnBarredException extends FinanceException
{
    private static final long serialVersionUID = 21062011133816L;

    /**
     * Non-default constructor
     * 
     * @param message
     * @param rootException
     */
    public MsisdnBarredException(String message, Exception rootException)
    {
        super(message, rootException);
    }

    public MsisdnBarredException(String message)
    {
        super(message);
    }
}
