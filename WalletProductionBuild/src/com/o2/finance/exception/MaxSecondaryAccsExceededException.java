package com.o2.finance.exception;

/**
 * Purpose: Thrown when the createUser call fails due to the max number of secondary accounts associated with the primary account
 * has been exceeded com.o2.finance.exception User: Vijay Kancherla Date: 21/06/2011
 */
public class MaxSecondaryAccsExceededException extends FinanceException
{
    private static final long serialVersionUID = 21062011132316L;

    /**
     * Non-default constructor
     * 
     * @param message
     * @param rootException
     */
    public MaxSecondaryAccsExceededException(String message, Exception rootException)
    {
        super(message, rootException);
    }

    public MaxSecondaryAccsExceededException(String message)
    {
        super(message);
    }
}
